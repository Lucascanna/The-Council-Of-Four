package modelDTOTests;

import static org.junit.Assert.*;

import java.util.ArrayList;
import java.util.List;

import org.junit.Test;

import client.modelDTO.gameTableDTO.GenericPlayerDTO;
import client.modelDTO.gameTableDTO.PermitTileDTO;

public class GenericPlayerDTOTest {

	@Test
	public void testGettersAndSetters() {
		String name="Andre";
		int playerNumber=1;
		int assistants=0;
		int score=3;
		int nobility=0;
		int coins=5;
		int emporiums=10;
		int hand=2;
		List<PermitTileDTO> availablePermitTiles= new ArrayList<>();
		GenericPlayerDTO player= new GenericPlayerDTO();
		player.setAssistants(assistants);
		player.setAvailablePermitTiles(availablePermitTiles);
		player.setCoins(coins);
		player.setEmporiums(emporiums);
		player.setHand(hand);
		player.setName(name);
		player.setNobility(nobility);
		player.setPlayerNumber(playerNumber);
		player.setScore(score);
		assertTrue(player.getAssistants()==assistants);
		assertTrue(player.getAvailablePermitTiles()==availablePermitTiles);
		assertTrue(player.getCoins()==coins);
		assertTrue(player.getEmporiums()==emporiums);
		assertTrue(player.getHand()==hand);
		assertTrue(player.getName()==name);
		assertTrue(player.getNobility()==nobility);
		assertTrue(player.getPlayerNumber()==playerNumber);
		assertTrue(player.getScore()==score);
		assertEquals("\nPlayer " + playerNumber + "\tname: "+ name + "\tassistants=" + assistants + "\tscore="
				+ score + "\tnobility=" + nobility + "\tcoins=" + coins + "\thand="+hand+"\tplayersPermitTilesTurnedUp=" + availablePermitTiles +
				 "\temporiums=" + emporiums + "]", player.toString());
	}

}
