package gameStuff;

public class CardColour {

	private String colour;
	
	/**
	 * constructor of the colour of the card
	 * @param colour is the name of the colour of the card
	 */
	public CardColour(String colour) {
		this.colour=colour;	
	}
	
	public String getColour() {
		return colour;
	}
		
}